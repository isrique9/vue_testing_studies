<!-- src/components/Counter.vue -->

<template>
  <div>
    <p data-testid="count">Contagem: {{ count }}</p>
    <button @click="increment">Incrementar</button>
  </div>
</template>

<script setup lang="ts">
import { ref } from 'vue'

const count = ref(0)
const increment = () => count.value++
</script>